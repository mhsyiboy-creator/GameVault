export function translate(locale, path, fallback = '') {
  const keys = String(path).split('.')
  const message = keys.reduce((current, key) => (current && current[key] != null ? current[key] : null), MESSAGES[locale])
  if (message != null) {
    return message
  }
  return fallback || path
}

export function formatMessage(locale, path, values = {}) {
  const message = translate(locale, path, '')
  return Object.keys(values).reduce((text, key) => text.replace(`{${key}}`, values[key]), message)
}
